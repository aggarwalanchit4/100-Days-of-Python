import math as m
num = int(input("Enter number: "))
power = int(input("Enter power: "))


print("Square Root :", m.sqrt(num))
print("Factorial   :", m.factorial(num))
print("Ceil        :", m.ceil(num))
print("Pi          :", m.pi)
print("Power       :", m.pow(num, power))
print("Floor       :", m.floor(num))