# greetings.py

def greet(name):
    """Display a welcome message."""
    print(f"Hello, {name}! Welcome to Python.")


def morning(name):
    """Display a good morning message."""
    print(f"Good Morning, {name}! Have a wonderful day.")


def afternoon(name):
    """Display a good afternoon message."""
    print(f"Good Afternoon, {name}! Hope you're doing well.")


def evening(name):
    """Display a good evening message."""
    print(f"Good Evening, {name}! Have a relaxing evening.")


def goodbye(name):
    """Display a goodbye message."""
    print(f"Goodbye, {name}! See you again.")